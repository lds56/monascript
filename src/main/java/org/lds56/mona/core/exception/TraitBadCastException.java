public class TraitBadCastException extends MonaRuntimeException {

  public TraitBadCastException() {
    super();
  }

  public TraitBadCastException(String message, Throwable cause) {
    super(message, cause);
  }

  public TraitBadCastException(String message) {
    super(message);
  }

  public TraitBadCastException(Throwable cause) {
    super(cause);
  }

}
