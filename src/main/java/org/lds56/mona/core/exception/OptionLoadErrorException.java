package org.lds56.mona.core.exception;

/**
 * Load option exception
 * 
 */
public class OptionLoadErrorException extends MonaEngineException {


  public OptionLoadErrorException() {
    super();
  }


  public OptionLoadErrorException(String message, Throwable cause) {
    super(message, cause);
  }


  public OptionLoadErrorException(String message) {
    super(message);
  }


  public OptionLoadErrorException(Throwable cause) {
    super(cause);
  }

}
